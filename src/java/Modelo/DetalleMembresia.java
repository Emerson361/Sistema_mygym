package Modelo;

/**
 *
 * @author EMERSOM APAZA
 */
public class DetalleMembresia {
    int id;
    String fecha;
    String fechaTermino;
    String horario;

    public DetalleMembresia() {
    }

    public DetalleMembresia(int id, String fecha, String fechaTermino, String horario) {
        this.id = id;
        this.fecha = fecha;
        this.fechaTermino = fechaTermino;
        this.horario = horario;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getFechaTermino() {
        return fechaTermino;
    }

    public void setFechaTermino(String fechaTermino) {
        this.fechaTermino = fechaTermino;
    }

    public String getHorario() {
        return horario;
    }

    public void setHorario(String horario) {
        this.horario = horario;
    }
    
    
}
